#define S5PC100_UBRDIV1			(0xEC000428)
#define S5PC100_UDIVSLOT1		(0xEC00042C)
#define S5PC100_UINTP1			(0xEC000430)
#define S5PC100_UINTSP1			(0xEC000434)
#define S5PC100_UINTM1			(0xEC000438)


